//C++ code to check if a number is even or odd 

#include<iostream>
using namespace std;

int main()
{
	cout << "Enter a number : ";
	int num;
	cin >> num;

	if(num % 2 == 0)
		cout << "It is even number\n";
	else
		cout << "It is odd number\n";

	return 0;
}



