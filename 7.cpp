//C++ code to calculate average of 3 numbers

#include<iostream>
using namespace std;

int main()
{
	cout << "Enter first Number : ";
	float num1;
	cin >> num1;

	cout << "Enter second Number : ";
	float num2;
	cin >> num2;

	cout << "Enter third Number : ";
	float num3;
	cin >> num3;

	float average = (num1 + num2 + num3)/3 ;

	cout << "Average == " << average << endl;

	return 0;
}


