//C++ code to compare 2 numbers

#include<iostream>
using namespace std;

int main()
{
	cout << "Enter first number : ";
	int num1;
	cin >> num1;


	cout << "Enter second number : ";
	int num2;
	cin >> num2;

	if(num1 > num2)
		cout << "First number is greater than second\n";
	else if(num2 > num1)
		cout << "Second number is greate than first\n";
	else
		cout << "Both numbers are equal\n";

	return 0;
}


