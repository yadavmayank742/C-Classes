//C++ code to calculate area of a rectangle

#include<iostream>
using namespace std;

int main()
{
	cout << "Enter Length of the rectangle : ";
	float length;
	cin >> length;

	cout << "Enter Breadth of the rectangle : ";
	float breadth;
	cin >> breadth;

	float area = length * breadth;

	cout << "Area == " << area << endl;

	return 0;

}


