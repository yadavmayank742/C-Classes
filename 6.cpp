// C++ code to check if a number is +ve, -ve, or 0

#include<iostream>
using namespace std;

int main()
{
	cout << "Enter a number : ";
	int num;
	cin >> num;

	if(num > 0)
		cout << "number is positive\n";
	else if(num < 0)
		cout << "number is negative\n";
	else
		cout << "number is zero\n";

	return 0;

}


