//C++ code to calculate volume of a cuboid

#include<iostream>
using namespace std;

int main()
{
	cout << "Enter Length of the Cuboid : ";
	float length;
	cin >> length;

	cout << "Enter Width of the Cuboid : ";
	float width;
	cin >> width;

	cout << "Enter Height of the Cuboid : ";
	float height;
	cin >> height;

	float volume = length * width * height ;

	cout << "Volume == " << volume << endl;

	return 0;

}


