//C++ code to check if given year is leap or not

#include<iostream>
using namespace std;

int main()
{
	cout << "Enter Year : ";
	int year;
	cin >> year;

	if(year % 4 == 0)
		cout << "It is leap year\n";
	else
		cout << "It is not a leap year\n";
	return 0;
}


