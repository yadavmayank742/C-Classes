//C++ code to check if a number is divisible by 7 or not

#include<iostream>
using namespace std;

int main()
{
	cout << "Enter a Number : ";
	int num;
	cin >> num;

	if(num % 7 == 0)
		cout << "Number is divisible by 7\n";
	else
		cout << "Number is not divisible by 7\n";

	return 0;
}


